#include <Arduino.h>
#include "GBHelper.h"

//******************************************************************************************************************//
//* Lettura titolo del gioco
//******************************************************************************************************************//
String readCartTitle() {
  char title[11];
  for (int i = 0; i < 11; i++) {
    unsigned int addr = 0x0134 + i;
    title[i] = (char)readByte(addr);
  }
  title[15] = '\0';

  return title;
}

//******************************************************************************************************************//
//* Lettura codice del gioco
//******************************************************************************************************************//
String readCartCode() {
  char code[4];
  for (int i = 0; i < 4; i++) {
    unsigned int addr = 0x013F + i;
    code[i] = (char)readByte(addr);
  }
  code[15] = '\0';

  return code;
}

//******************************************************************************************************************//
//* Lettura tipo cartuccia
//******************************************************************************************************************//
int readCartType() {
  int cartType = readByte(0x0147);

  return cartType;
}

//******************************************************************************************************************//
//* Lettura dimensione ROM
//******************************************************************************************************************//
int readROMSize() {
  int romSize = readByte(0x0148);

  return romSize;
}

//******************************************************************************************************************//
//* Lettura dimensione RAM
//******************************************************************************************************************//
int readRAMSize() {
  int ramSize = readByte(0x0149);

  return ramSize;
}

